import { SelectOptions } from '../../types/SelectOptions';

type Props = {
  selectedOption: SelectOptions;
  handleSelectChange: (newValue: SelectOptions) => void;
  query: string;
  handleQueryChange: (newValue: string) => void;
};

export const TodoFilter: React.FC<Props> = ({
  selectedOption,
  handleSelectChange: onSelectChange = () => {},
  query,
  handleQueryChange: onQueryChange = () => {},
}) => {
  return (
    <form className="field has-addons">
      <p className="control">
        <span className="select">
          <select
            data-cy="statusSelect"
            value={selectedOption}
            onChange={e => onSelectChange(e.target.value as SelectOptions)}
          >
            <option value="all">All</option>
            <option value="active">Active</option>
            <option value="completed">Completed</option>
          </select>
        </span>
      </p>

      <p className="control is-expanded has-icons-left has-icons-right">
        <input
          data-cy="searchInput"
          type="text"
          className="input"
          placeholder="Search..."
          value={query}
          onChange={e => onQueryChange(e.target.value)}
        />
        <span className="icon is-left">
          <i className="fas fa-magnifying-glass" />
        </span>

        {query && (
          <span className="icon is-right" style={{ pointerEvents: 'all' }}>
            <button
              data-cy="clearSearchButton"
              type="button"
              className="delete"
              onClick={() => onQueryChange('')}
            />
          </span>
        )}
      </p>
    </form>
  );
};
